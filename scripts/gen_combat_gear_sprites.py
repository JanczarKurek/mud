def make_bow() -> Image.Image:
    """Vertical shortbow — wooden limbs curving to the right, leather-wrapped
    grip in the middle, taut string running straight up the left side."""
    img = new_img()
    px, rect = helpers(img)
    ground_shadow(rect, 16, 28, 5)

    wood_dark = (75, 45, 22, 255)
    wood = (140, 95, 50, 255)
    wood_hi = (200, 150, 90, 255)
    grip_dark = (45, 25, 12, 255)
    grip = (90, 55, 28, 255)
    nock = (55, 35, 18, 255)
    string = (240, 230, 200, 255)

    # Bow limb curve (x position by y), bowing to the right.
    bow_col_by_y = {
        3: 16, 4: 15, 5: 15, 6: 16, 7: 17, 8: 18, 9: 18,
        10: 19, 11: 19, 12: 19,
        13: 18, 14: 17, 15: 17,
        16: 17, 17: 17,
        18: 17, 19: 17, 20: 18,
        21: 19, 22: 19, 23: 19,
        24: 18, 25: 17, 26: 16, 27: 15, 28: 15,
    }
    for y, x in bow_col_by_y.items():
        px(x - 1, y, wood_dark)
        px(x, y, wood)
        px(x + 1, y, wood_hi)

    # Nocks at tip and tail
    px(15, 2, nock)
    px(16, 2, nock)
    px(15, 29, nock)
    px(16, 29, nock)

    # Bowstring — straight line down the left side, from nock to nock
    for y in range(3, 29):
        px(14, y, string)

    # Leather grip wrap in the middle (covers limbs from y=14 to y=18)
    rect(16, 14, 3, 5, grip)
    rect(16, 14, 3, 1, grip_dark)
    rect(16, 18, 3, 1, grip_dark)
    # Grip wrap stripes
    for y in (15, 17):
        px(16, y, grip_dark)
        px(18, y, grip_dark)

    return img


def make_crossbow() -> Image.Image:
    """Horizontal crossbow with stock (butt at right), curved metal prod at
    the front (left), bowstring across the prod tips, and a small trigger
    guard underneath."""
    img = new_img()
    px, rect = helpers(img)
    ground_shadow(rect, 16, 26, 10)

    stock_dark = (60, 38, 18, 255)
    stock = (115, 75, 38, 255)
    stock_hi = (170, 125, 75, 255)
    metal_dark = (55, 55, 65, 255)
    metal = (125, 125, 140, 255)
    metal_hi = (200, 200, 215, 255)
    string = (240, 230, 200, 255)
    trigger = (150, 110, 50, 255)

    # Stock — horizontal beam from left (front) to right (butt)
    rect(8, 15, 17, 4, stock)
    rect(8, 15, 17, 1, stock_hi)
    rect(8, 18, 17, 1, stock_dark)

    # Butt taper at the right end (rounded shoulder)
    px(25, 15, BG)
    px(25, 18, BG)
    px(24, 15, stock_dark)
    px(24, 18, stock_dark)
    # Butt cap
    rect(25, 16, 2, 2, stock)
    px(26, 16, stock_dark)
    px(26, 17, stock_dark)

    # Front cap (metal collar where the prod mounts)
    rect(7, 14, 2, 6, metal_dark)
    rect(8, 14, 1, 6, metal)
    px(8, 14, metal_hi)

    # Prod (the bow arms) — curved arc mounted at the front,
    # tips reaching up to ~y=9 and down to ~y=24
    prod_pts_top = [
        (8, 13), (7, 12), (6, 11), (5, 10),
        (5, 9), (6, 8), (7, 8), (8, 8), (9, 9),
    ]
    prod_pts_bot = [
        (8, 20), (7, 21), (6, 22), (5, 23),
        (5, 24), (6, 25), (7, 25), (8, 25), (9, 24),
    ]
    for (x, y) in prod_pts_top + prod_pts_bot:
        px(x, y, metal)
        px(x, y - 1 if (x, y) in prod_pts_top else y + 1, metal_hi)
        px(x + 1, y, metal_dark)

    # Bowstring — drawn taut from prod tip to prod tip, hooked over the trigger
    # Top half: from (6, 8) diagonally down to (16, 16)
    for y in range(9, 17):
        x = 6 + (y - 9)
        px(x, y, string)
    # Bottom half: from (16, 17) diagonally down to (6, 25)
    for y in range(17, 26):
        x = 6 + (25 - y)
        px(x, y, string)
    # Hook over the trigger nut
    px(16, 16, string)
    px(16, 17, string)

    # Bolt groove down the top of the stock
    for x in range(10, 24):
        px(x, 15, stock_dark)
    # Bolt sitting in the groove (visible nub)
    rect(11, 14, 4, 1, metal_hi)
    px(15, 14, metal)

    # Trigger guard
    rect(15, 19, 5, 2, stock_dark)
    px(15, 21, stock_dark)
    px(19, 21, stock_dark)
    # Trigger lever
    px(17, 21, trigger)
    px(18, 21, trigger)
    px(17, 22, trigger)

    return img



        rect(ox + 2, 10, 5, 8, leather)
        # Cuff at top
        rect(ox + 2, 10, 5, 2, cuff)
        rect(ox + 2, 10, 5, 1, leather_dark)
        rect(ox + 2, 11, 5, 1, cuff_hi)

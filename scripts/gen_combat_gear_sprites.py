"""
Generates static 32x32 PNG sprites for the combat-gear pickup icons that
previously shipped as 8x8 codex placeholders, plus the bow and crossbow
(previously generated by gen_ranged_assets.py).

Covers:
  bronze_sword, leather_armor, leather_helmet, leather_legs,
  traveler_boots, wooden_shield, bow, crossbow

Each object gets a single `sprite.png` in its overworld_objects directory.
Style matches gen_gathering_sprites.py and gen_casting_item_sprites.py —
chunky pixels, 2-3 shading levels per material, no anti-aliasing, transparent
background, small ground shadow under each item.
"""

from __future__ import annotations

import os
from PIL import Image

W, H = 32, 32
BG = (0, 0, 0, 0)
SHADOW = (0, 0, 0, 70)


def new_img() -> Image.Image:
    return Image.new("RGBA", (W, H), BG)


def helpers(img: Image.Image):
    def px(x: int, y: int, c):
        if 0 <= x < W and 0 <= y < H:
            img.putpixel((x, y), c)

    def rect(x: int, y: int, w: int, h: int, c):
        for dy in range(h):
            for dx in range(w):
                px(x + dx, y + dy, c)

    return px, rect


def ground_shadow(rect, cx: int, cy: int, half: int) -> None:
    rect(cx - half, cy, half * 2 + 1, 1, SHADOW)
    inner = max(half - 2, 1)
    rect(cx - inner, cy + 1, inner * 2 + 1, 1, SHADOW)



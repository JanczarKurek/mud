def make_bronze_sword() -> Image.Image:
    """Diagonal sword from lower-left grip to upper-right tip. Leather-wrapped
    grip, brass crossguard, long bronze blade with a steel-tinted edge."""
    img = new_img()
    px, rect = helpers(img)
    ground_shadow(rect, 16, 27, 8)

    grip_dark = (50, 30, 15, 255)
    grip = (110, 70, 35, 255)
    grip_hi = (160, 110, 65, 255)
    pommel_dark = (55, 35, 20, 255)
    pommel = (180, 145, 70, 255)
    pommel_hi = (230, 200, 110, 255)
    guard_dark = (130, 90, 30, 255)
    guard = (215, 165, 70, 255)
    guard_hi = (245, 215, 130, 255)

    bronze_dark = (115, 75, 35, 255)
    bronze = (190, 135, 65, 255)
    bronze_hi = (240, 200, 130, 255)
    edge = (255, 240, 200, 255)

    # Pommel (round cap at the butt of the grip)
    px(5, 26, pommel_dark)
    px(6, 26, pommel)
    px(6, 27, pommel_dark)
    px(7, 26, pommel_hi)
    px(7, 27, pommel)

    # Grip (diagonal leather wrap, 5 steps)
    grip_pts = [(7, 25), (8, 24), (9, 23), (10, 22), (11, 21)]
    for (x, y) in grip_pts:
        px(x, y, grip)
        px(x - 1, y, grip_dark)
        px(x, y + 1, grip_dark)
        px(x + 1, y, grip_hi)
    # Wrap stripes (alternating darker bands)
    px(7, 24, grip_dark)
    px(9, 22, grip_dark)

    # Crossguard — short bar perpendicular to the blade at (11, 21)
    # Goes from (9, 23) up-right to (13, 19) — opposite diagonal
    guard_pts = [(9, 23), (10, 22), (11, 21), (12, 20), (13, 19)]
    for (x, y) in guard_pts:
        px(x, y, guard)
    # Highlight on the upper side of the guard
    px(10, 22, guard_hi)
    px(11, 21, guard_hi)
    px(12, 20, guard_hi)
    # Shadow on the lower side
    px(9, 24, guard_dark)
    px(13, 20, guard_dark)
    # Slight knobs at each end of the guard
    px(8, 24, guard_dark)
    px(14, 18, guard_hi)

    # Blade — long diagonal from (12, 20) up-right to (24, 8)
    blade_pts = [
        (12, 20), (13, 19), (14, 18), (15, 17), (16, 16), (17, 15),
        (18, 14), (19, 13), (20, 12), (21, 11), (22, 10), (23, 9), (24, 8),
    ]
    for (x, y) in blade_pts:
        # Spine (lower-right side of the diagonal)
        px(x + 1, y, bronze_dark)
        # Center body
        px(x, y, bronze)
        # Upper edge (highlight)
        px(x, y - 1, bronze_hi)
        # Outermost edge — bright steel sheen
        px(x - 1, y - 1, edge)
    # Pointed tip
    px(25, 7, bronze_hi)
    px(24, 7, edge)
    px(25, 8, bronze_dark)

    return img


def make_wooden_shield() -> Image.Image:
    """Round wooden shield viewed face-on, with horizontal plank seams and a
    central iron boss."""
    img = new_img()
    px, rect = helpers(img)
    ground_shadow(rect, 16, 27, 9)

    rim_dark = (50, 30, 15, 255)
    rim = (90, 55, 25, 255)
    wood_dark = (95, 60, 28, 255)
    wood = (150, 100, 55, 255)
    wood_hi = (200, 150, 95, 255)
    seam = (75, 45, 22, 255)
    iron_dark = (50, 50, 60, 255)
    iron = (115, 115, 130, 255)
    iron_hi = (200, 200, 215, 255)

    # Body — disc roughly centered on (16, 16), radius ~10
    rect(8, 8, 17, 17, wood)
    # Round off corners
    for (x, y) in [(8, 8), (24, 8), (8, 24), (24, 24)]:
        px(x, y, BG)
    for (x, y) in [(9, 8), (8, 9), (23, 8), (24, 9),
                   (9, 24), (8, 23), (23, 24), (24, 23),
                   (10, 7), (10, 25), (22, 7), (22, 25),
                   (7, 10), (25, 10), (7, 22), (25, 22)]:
        px(x, y, BG)
    # Outer rim
    for x in range(11, 22):
        px(x, 7, rim)
        px(x, 25, rim)
    for y in range(11, 22):
        px(7, y, rim)
        px(25, y, rim)
    # Rim corner pixels (rounded)
    for (x, y) in [(9, 9), (10, 8), (8, 10),
                   (22, 8), (23, 9), (24, 10),
                   (8, 22), (9, 23), (10, 24),
                   (22, 24), (23, 23), (24, 22)]:
        px(x, y, rim)
    # Rim shadow on the inside edge
    for (x, y) in [(11, 8), (12, 8), (20, 8), (21, 8),
                   (8, 11), (8, 12), (8, 20), (8, 21),
                   (24, 11), (24, 12), (24, 20), (24, 21),
                   (11, 24), (12, 24), (20, 24), (21, 24)]:
        px(x, y, rim_dark)

    # Plank seams — horizontal lines across the face
    for x in range(9, 24):
        if 9 <= x <= 23:
            px(x, 12, seam)
            px(x, 20, seam)

    # Plank highlights — upper-left of each plank
    for x in range(10, 23):
        if 9 <= x <= 23:
            px(x, 9, wood_hi)
            px(x, 13, wood_hi)
    # Plank shadows — lower-right
    for x in range(10, 23):
        if 9 <= x <= 23:
            px(x, 19, wood_dark)
            px(x, 23, wood_dark)

    # Wood grain flecks
    for (x, y) in [(11, 10), (14, 11), (18, 10), (21, 11),
                   (12, 15), (17, 16), (20, 14), (13, 17),
                   (11, 21), (15, 22), (20, 21), (18, 22)]:
        px(x, y, wood_dark)

    # Central iron boss — 5x5 rounded disc at (14..18, 14..18)
    rect(14, 14, 5, 5, iron)
    px(14, 14, BG); px(18, 14, BG)
    px(14, 18, BG); px(18, 18, BG)
    # Soften corners with mid tone
    px(14, 14, iron_dark)
    px(18, 14, iron_dark)
    px(14, 18, iron_dark)
    px(18, 18, iron_dark)
    # Highlight glint top-left
    px(15, 15, iron_hi)
    px(16, 15, iron_hi)
    px(15, 16, iron_hi)
    # Shadow bottom-right
    px(17, 17, iron_dark)
    px(17, 16, iron_dark)
    px(16, 17, iron_dark)
    # Boss outline
    for (x, y) in [(15, 14), (16, 14), (17, 14),
                   (14, 15), (14, 16), (14, 17),
                   (18, 15), (18, 16), (18, 17),
                   (15, 18), (16, 18), (17, 18)]:
        if img.getpixel((x, y))[3] == 0 or img.getpixel((x, y)) == iron:
            pass

    return img



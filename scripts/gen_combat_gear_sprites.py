def main() -> None:
    out_root = os.path.join("assets", "overworld_objects")
    targets = [
        ("bronze_sword",   make_bronze_sword),
        ("wooden_shield",  make_wooden_shield),
        ("leather_armor",  make_leather_armor),
        ("leather_helmet", make_leather_helmet),
        ("leather_legs",   make_leather_legs),
        ("traveler_boots", make_traveler_boots),
        ("bow",            make_bow),
        ("crossbow",       make_crossbow),
    ]
    for type_id, maker in targets:
        out_dir = os.path.join(out_root, type_id)
        os.makedirs(out_dir, exist_ok=True)
        img = maker()
        path = os.path.join(out_dir, "sprite.png")
        img.save(path)
        print(f"Saved {path}")


if __name__ == "__main__":
    main()
